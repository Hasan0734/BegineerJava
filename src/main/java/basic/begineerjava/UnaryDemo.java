public class UnaryDemo {
    
    public static void main(String[] args) {
        
        int x = 10;
        int result;
        
        result = +x;      
        System.out.println("Result: " +result);
        
        result = -x;      
        System.out.println("Result: " +result);
        
        result = ++x;      
        System.out.println("Result: " +result);
        
        result = --x;      
        System.out.println("Result: " +result);
    }
}
